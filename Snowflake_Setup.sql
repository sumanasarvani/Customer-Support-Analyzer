--> Clean TICKET_DESCRIPTION before sentiment analysis
CREATE OR REPLACE TABLE RAW_TICKETS_CLEANED AS
SELECT 
    *,
    REPLACE(TICKET_DESCRIPTION, '{product_purchased}', PRODUCT_PURCHASED) AS TICKET_DESCRIPTION_CLEAN
FROM RAW_TICKETS;

--> Verify to see if the replacement worked
